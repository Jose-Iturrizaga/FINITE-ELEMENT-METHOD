function [dN] = ShapeDerivQ8(xi)

dN = zeros(2,8);
dN(1,1)= 1/4*(1-xi(2))*(2*xi(1)+xi(2));
dN(1,2)= 1/4*(1-xi(2))*(2*xi(1)-xi(2));
dN(1,3)= 1/4*(1+xi(2))*(2*xi(1)+xi(2));
dN(1,4)= 1/4*(1+xi(2))*(2*xi(1)-xi(2));
dN(1,5)= -xi(1)*(1-xi(2));
dN(1,6)=  1/2*(1-xi(2)^2);
dN(1,7)= -xi(1)*(1+xi(2));
dN(1,8)= -1/2*(1-xi(2)^2);
dN(2,1)= 1/4*(1-xi(1))*(2*xi(2)+xi(1));
dN(2,2)= 1/4*(1+xi(1))*(2*xi(2)-xi(1));
dN(2,3)= 1/4*(1+xi(1))*(2*xi(2)+xi(1));
dN(2,4)= 1/4*(1-xi(1))*(2*xi(2)-xi(1));
dN(2,5)= -1/2*(1-xi(1)^2);
dN(2,6)= -xi(2)*(1+xi(1));
dN(2,7)=  1/2*(1-xi(1)^2);
dN(2,8)= -xi(2)*(1-xi(1));
return